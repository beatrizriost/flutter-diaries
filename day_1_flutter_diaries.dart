void main() {
  
  // 1 - Introdução a variáveis 
  
  String variavelNome = "Beatriz";
  print (variavelNome);
  
  // o valor de uma string deve ser inserido entre aspas duplas ""
  
  int valorVariavel = 10;
  print (valorVariavel);
  
  // int = numeros inteiros
  
  bool ehVerdadeiro = true;
  print (ehVerdadeiro);
  
  // bool = booleano (v ou f)
  
  List<String> listaDePalavras = ["Beatriz", "Rios"];
  print ('${listaDePalavras[0]}-${listaDePalavras[1]}');
  
}
